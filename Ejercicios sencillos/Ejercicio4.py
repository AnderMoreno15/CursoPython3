# Dados 2 numeros mostrar la suma, resta, division y multiplicacion de ambos
num1 = float(input("Número 1: "))
num2 = float(input("Número 2: "))
print("Suma: %.2f" % (num1 + num2))
print("Resta: %.2f" % (num1 - num2))
print("Multiplicación: %.2f" % (num1 * num2))
print("División: %.2f" % (num1 / num2))